<h2 class="topic">Writers' Gradation and Work Quality Check</h2>
<p>All new writers are accepted as freshmen and can only take the easiest orders that 
    do not require deep research or much writing. As soon as he or she passes the 
    trial period without any negative feedback, their status is raised and the 
    writer becomes eligible for taking more complicated orders.</p>
<p>All writers and their work are carefully monitored. Our Quality Assurance 
    Department makes sure that each writer systematically undergoes through 
    the control of clients' feedback, non-plagiarism, and on-time submission 
    of the accepted projects. To prevent delays in submissions, we contact writers 
    if the completed paper is not uploaded 10% of the time before the deadline 
    to remind them that the deadline is coming up.</p>
<p>We also pay close attention to every writer's activity, contact those who neglect 
    taking orders for a long time, and try to solve individual issues that might occur.</p>
<p>Thus, if a writer's performance is getting worse, all the necessary measures are taken.</p>

